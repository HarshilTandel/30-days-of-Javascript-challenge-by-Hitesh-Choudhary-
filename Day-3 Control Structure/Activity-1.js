function checkNum(nums) {
  if (nums > 0) {
    console.log(` ${nums} is Positive`);
  } else if (nums < 0) {
    console.log(`${nums} is Negative`);
  } else if (nums == 0) {
    console.log(` ${nums} is Zero`);
  }
}

checkNum(1);
checkNum(-1);
checkNum(0);

